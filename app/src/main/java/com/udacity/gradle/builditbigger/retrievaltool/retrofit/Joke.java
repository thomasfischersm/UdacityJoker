package com.udacity.gradle.builditbigger.retrievaltool.retrofit;

/**
 * A Retrofit joke object.
 */
public class Joke {

    private String id;
    private String joke;
    private String status;

    public String getId() {
        return id;
    }

    public String getJoke() {
        return joke;
    }

    public String getStatus() {
        return status;
    }
}
